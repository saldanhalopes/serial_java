    public void iniciarPortas() {
        int i = 0;
        CommPortIdentifier portaId = null;
        Enumeration portaEnum = CommPortIdentifier.getPortIdentifiers();
        String[] port_name_list = new String[5];

        while (portaEnum.hasMoreElements()) {
            CommPortIdentifier atualPortaId = (CommPortIdentifier) portaEnum.nextElement();
            port_name_list[i] = atualPortaId.getName();
            portaId = atualPortaId;
            i++;
        }

        cmbPortas.removeAllItems();
        for (String port : port_name_list) {
            if (port != null) {
                cmbPortas.addItem(port);
            }
        }
        if (portaId == null) {
            cmbPortas.addItem("Portas n√£o encontradas");
        }
    }

